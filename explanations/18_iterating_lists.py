animals = [
    "dog",
    "cat",
    "bird",
    "elephant",
    "tiger",
    "snail",
    "horse",
    "shark",
    "eagle",
]

# Iterate over the list and print each animal name
for animal in animals:
    print(animal)

# Using enumerate to print each animal with its index as a tuple
for index, animal in enumerate(animals):
    print(index, animal)
